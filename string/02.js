var nome = "AgileCode";

// exibe o elemento na posição indicada 
console.log(nome.charAt(2));

// exibe o char code do elemento na posição indicada
console.log(nome.charCodeAt(0));

// concatena o elemento indicado com a String
console.log(nome.concat("!"));

// retorna a posição do elemento indicado
console.log(nome.indexOf("e"));

// substituí o elemento do primeiro parâmentro pelo elemento do segundo
console.log(nome.replace("Code", "!"));

// corta a string gerando novas a partir do elemento indicado
console.log(nome.split("e"));