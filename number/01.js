var nota = 10;

// faz cálculos de potências
nota.toExponential(2);

// arredonda até o numero informado na função 
nota.toFixed(2);

// arredonda até o numero informado na função 
nota.toPrecision(1);

// transforma o number em um dado String
nota.toString();

// mostra o valor da variavel
nota.valueOf();


// Math API

// valor absoluto do número
Math.abs(nota);

// valor inteiro do número
Math.floor(nota);

// logarítmo natural do número
Math.log(nota);

// retorna o menor número
Math.min(nota);

// retorna o maior número
Math.max(nota);

// potência do número
Math.pow(nota);

// gera um número aleatório
Math.random(nota);

// arredonda para o número inteiro mais próximo
Math.round(nota);

//seno do número
Math.sin(nota);

// raíz quadrada do número
Math.sqrt(nota);

// tangente do número
Math.tan(nota);